export function translateText(value, language) {
  if (language !== 'fr' || typeof value !== 'string') return value;
  return translations.fr[value] || value;
}

export function translateNode(children, language) {
  if (typeof children === 'string') return translateText(children, language);
  if (Array.isArray(children)) return children.map((child) => translateNode(child, language));
  return children;
}
